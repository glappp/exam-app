async function main() {
  const allQuestions = [
    ...readTableQuestions,
    ...barChartQuestions,
    ...readChartQuestions,
    ...wordProblemQuestions,
  ];

  for (const q of allQuestions) {
    await prisma.question.create({ data: q });
  }

  const counts = {
    'subtopic:statistics-read-table': readTableQuestions.length,
    'subtopic:statistics-bar-chart': barChartQuestions.length,
    'subtopic:statistics-read-chart': readChartQuestions.length,
    'subtopic:statistics-word-problem': wordProblemQuestions.length,
  };

  console.log(`✅ สร้างข้อสอบ ${allQuestions.length} ข้อ (topic:statistics, p4)`);
  for (const [k, v] of Object.entries(counts)) {
    console.log(`   ${k}: ${v} ข้อ`);
  }
}

main()
  .catch(e => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
