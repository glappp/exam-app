async function main() {
  console.log('🚀 กำลัง seed ข้อสอบ สสวท คณิตศาสตร์ ป.6 พ.ศ. 2554...');

  // ตรวจว่า R2 URL ถูกตั้งค่าแล้ว
  if (R2.includes('REPLACE_ME')) {
    console.warn('⚠️  R2 URL ยังเป็น placeholder — อัปเดต const R2 ก่อนรันจริง');
    console.warn('   รูปภาพจะแสดงเป็น broken image จนกว่าจะอัปโหลดไปยัง R2');
  }

  let count = 0;
  for (const q of questions) {
    await prisma.question.create({ data: q });
    count++;
    process.stdout.write(`\r   สร้างข้อ ${count}/${questions.length}...`);
  }
  console.log(`\n✅ สร้างข้อสอบ ${count} ข้อ สำเร็จ`);

  // สรุป subtopic
  const summary = {};
  for (const q of questions) {
    const sub = q.attributes?.subtopic?.[0] ?? 'unknown';
    summary[sub] = (summary[sub] || 0) + 1;
  }
  console.log('\n📊 สรุปตาม subtopic:');
  for (const [k, v] of Object.entries(summary).sort()) {
    console.log(`   ${k}: ${v} ข้อ`);
  }

  console.log('\n📝 หมายเหตุ:');
  console.log('   - ข้อที่มี "(โปรดตรวจสอบ...)" → ต้องแก้ textTh จากต้นฉบับ');
  console.log('   - ข้อที่มีรูป → อัปโหลด PNG ไปยัง R2 และอัปเดต const R2');
  console.log('   - ข้อมีรูป: 14, 16, 17, 18, 21, 23, 24, 25, 26, 27, 28, 29, 39, 40');
  console.log('   - ข้อ 29 มี hint image (29-2.png)');
  console.log('   - ข้อ 40 มีรูป 2 รูป (40-1.png bar, 40-2.png pie)');
}

main()
