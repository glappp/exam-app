function calcGrade(score) {
  if (score >= 80) return 4;
  if (score >= 70) return 3;
  if (score >= 60) return 2;
  if (score >= 50) return 1;
  return 0;
}

function parseName(fullName) {
  // ตัด prefix เด็กหญิง / เด็กชาย ออก แล้วแยก firstName / lastName
  const stripped = fullName.replace(/^เด็กหญิง|^เด็กชาย/, '').trim();
  const spaceIdx = stripped.indexOf(' ');
  if (spaceIdx === -1) return { firstName: stripped, lastName: '' };
  return {
    firstName: stripped.slice(0, spaceIdx),
    lastName: stripped.slice(spaceIdx + 1),
  };
}

function calcStats(scores) {
  const count = scores.length;
  const mean = scores.reduce((s, v) => s + v, 0) / count;
  const sd = Math.sqrt(scores.reduce((s, v) => s + (v - mean) ** 2, 0) / count);
  const passCount = scores.filter(v => v >= 50).length;
  return {
    mean: Math.round(mean * 100) / 100,
    sd: Math.round(sd * 100) / 100,
    count,
    passCount,
  };
}

