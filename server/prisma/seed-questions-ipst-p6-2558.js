async function main() {
  console.log(`🌱 เริ่ม seed ${SOURCE} (${questions.length} ข้อ)`);

  const existing = await prisma.question.findFirst({ where: { source: SOURCE } });
  if (existing) {
    console.log('⏭  มีโจทย์อยู่แล้ว — ข้าม');
    return;
  }

  for (const q of questions) {
    await prisma.question.create({ data: q });
  }
  console.log(`✅ สร้างโจทย์ ${questions.length} ข้อเรียบร้อย`);
}

main()
  .catch(e => { console.error('❌', e.message); process.exit(1); })
  .finally(() => prisma.$disconnect());
