async function main() {
  console.log('🚀 กำลัง seed ข้อสอบ สสวท คณิตศาสตร์ ป.6 พ.ศ. 2554 (ฉบับที่ 1)...');

  if (R2.includes('REPLACE_ME')) {
    console.warn('⚠️  R2 URL ยังเป็น placeholder — อัปเดต const R2 ก่อนรันจริง');
  }

  let count = 0;
  for (const q of questions) {
    await prisma.question.create({ data: q });
    count++;
    process.stdout.write(`\r   สร้างข้อ ${count}/${questions.length}...`);
  }
  console.log(`\n✅ สร้างข้อสอบ ${count} ข้อ สำเร็จ`);

  const summary = {};
  for (const q of questions) {
    const sub = q.attributes?.subtopic?.[0] ?? 'unknown';
    summary[sub] = (summary[sub] || 0) + 1;
  }
  console.log('\n📊 สรุปตาม subtopic:');
  for (const [k, v] of Object.entries(summary).sort()) {
    console.log(`   ${k}: ${v} ข้อ`);
  }
}

main()
  .catch(e => { console.error('❌', e.message); process.exit(1); })
  .finally(() => prisma.$disconnect());
